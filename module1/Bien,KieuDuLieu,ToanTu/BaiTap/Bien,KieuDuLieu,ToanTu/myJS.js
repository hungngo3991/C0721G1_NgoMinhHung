

// Bai 1

let phy = parseInt(prompt("Nhập vào điểm Vật lý"))
let che = parseInt(prompt("Nhập vào điểm Hóa học"))
let bio = parseInt(prompt("Nhập vào điểm Sinh học"))

let tong = phy + che + bio
let trungbinh = (phy + che + bio)/3
let tb = trungbinh.toFixed(2)
document.writeln("Tổng diểm = " +tong)
document.writeln("<br>")
document.writeln("Điểm trung bình = " +tb)

